package operadores;

public class Aritmetico {

    public static void main(String[] args) {
        int a = 9;
        int b = 3;
        int soma = a + b;
        System.out.println(soma);
        int subtracao = a - b;
        System.out.println(subtracao);
        double divisao = b / a;
        System.out.println(divisao);
        double multiplicacao = a * b;
        System.out.println(multiplicacao);
    }

}
